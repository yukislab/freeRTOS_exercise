/*FreeRTOS配置*/

/* START_TASK 任务 配置
 * 包括: 任务句柄 任务优先级 堆栈大小 创建任务
 */
#define START_TASK_PRIO       1//开始任务任务优先级
#define START_TASK_STACK_SIZE 128//单位是字，尽量大一点
TaskHandle_t start_task_handler;//任务句柄
void start_task(void* pvParameters);

/* TASK1 任务 配置
 * 包括: 任务句柄 任务优先级 堆栈大小 创建任务
 */
#define TASK1_PRIO       2//开始任务任务优先级
#define TASK1_STACK_SIZE 128//单位是字，尽量大一点
TaskHandle_t task1_handler;//任务句柄
void task1(void* pvParameters);

/******************************************************************************************************/

/**
 * @brief       FreeRTOS例程入口函数
 * @param       无
 * @retval      无
 */
TimerHandle_t timer1_handle;
TimerHandle_t timer2_handle;
void timer1_callback();
void timer2_callback();


void freertos_demo(void)
{                        
//任务创建函数
	//强制转换，防止警报
	xTaskCreate((TaskFunction_t        ) start_task,//指向开始任务函数的指针
				(char *                ) "start_task",//任务名字
			   (configSTACK_DEPTH_TYPE) START_TASK_STACK_SIZE,//开始任务堆栈大小
			   (void *                ) NULL,//入口参数
			   (UBaseType_t           ) START_TASK_PRIO,//任务优先级
			   (TaskHandle_t *        ) &start_task_handler );//任务句柄，就是任务的任务控制块
    vTaskStartScheduler();//开启任务调度器
}


//开始任务：创建任务，创建两个定时器（单次和周期）
void start_task(void* pvParameters)
{
	//先进入临界区
	taskENTER_CRITICAL();
	
	timer1_handle=xTimerCreate("timer1",500,pdFALSE,(void*)1,timer1_callback);
	timer2_handle=xTimerCreate("timer2",500,pdTRUE,(void*)1,timer2_callback);

	if(timer1_handle!=NULL&&timer2_handle!=NULL)
		printf("定时器创建成功！\n");
	
	xTaskCreate((TaskFunction_t        ) task1,//指向开始任务函数的指针
				(char *                ) "task1",//任务名字
			   (configSTACK_DEPTH_TYPE) TASK1_STACK_SIZE,//开始任务堆栈大小
			   (void *                ) NULL,//入口参数
			   (UBaseType_t           ) TASK1_PRIO,//任务优先级
			   (TaskHandle_t *        ) &task1_handler );//任务句柄，就是任务的任务控制块

			   
	//开始任务只执行一次，执行完删除自己，传入参数为NULL，代表删除自己，传入开始任务任务句柄也可以
    vTaskDelete(NULL);
    //退出临界区后再去调度
	taskEXIT_CRITICAL();
}

//任务1：按键扫描，开启、停止软件定时器
void task1(void* pvParameters)
{
	uint8_t key=0;
	while(1)
	{
		key=key_scan();

		if(key==KEY0_PRES)
		{
			 xTimerStart(timer1_handle,portMAX_DELAY);
			 xTimerStart(timer2_handle,portMAX_DELAY);
		}
		else if(key==KEY1_PRES)
		{
			 xTimerStop(timer1_handle,portMAX_DELAY);
			 xTimerStop(timer2_handle,portMAX_DELAY);
		}
		vTaskDelay(150);//系统自身的延时函数
	}
}

void timer1_callback()
{
	uint8_t count1=1;
	printf("单次定时器运行%d次\n",count1);
	count1++;
}
void timer2_callback()
{
	uint8_t count2=1;
	printf("周期定时器运行%d次\n",count2);
	count2++;

}
